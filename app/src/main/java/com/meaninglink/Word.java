package com.meaninglink;

public class Word {
    String phonetic;
    String meaning;

    public void setPhonetic(String phonetic) {
        this.phonetic = phonetic;
    }

    public void setMeaning(String meaning) {this.meaning = meaning; }

    public String getPhonetic() {
        return phonetic;
    }

    public String getMeaning() {
        return meaning;
    }
}
